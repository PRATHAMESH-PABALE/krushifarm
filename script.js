function changeLanguage(lang) {
    document.getElementById('title').innerText = translations[lang].title;
    document.getElementById('searchBox').placeholder = translations[lang].searchPlaceholder;

    const schemeKeys = Object.keys(translations[lang]);
    schemeKeys.forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            element.innerText = translations[lang][key];
        }
    });
}

document.getElementById('searchBox').addEventListener('keyup', function() {
    let filter = this.value.toLowerCase();
    let schemes = document.getElementsByClassName('scheme-card');

    for (let scheme of schemes) {
        let text = scheme.innerText.toLowerCase();
        scheme.style.display = text.includes(filter) ? 'block' : 'none';
    }
});

function showDetails(schemeName) {
    window.location.href = `details.html?scheme=${schemeName}`;
}
